    def start_test(self):
        print('Test Start ',self.selected_posid)
        if self.mode.get()==0:
            for i in range(self.num_sim):
                print(i,'th move in '+str(self.num_sim))
                for posid in self.posids:
                    print(posid)
                    self.ptl.schedule.request_target(posid, 'dTdP',  random.uniform(-360, 360), random.uniform(-190,190), log_note='')
                self.ptl.schedule._schedule_without_anticollision() # Make move_tables
                self.ptl.send_move_tables()
                 
        else:        
            self.pcomm.move('can0', self.selected_can, 'cw', 'cruise', 'theta', 400)
            time.sleep(4)
            theta_step=[0,90,90,90,90]
            phi_step=[0,45,45,45,45]
            theta_current=360.
            for i in range(len(theta_step)):
                self.pcomm.move('can0', self.selected_can, 'ccw', 'cruise', 'theta', theta_step[i])
                theta_current=theta_current-theta_step[i]
                time.sleep(1)
                self.pcomm.move('can0', self.selected_can, 'ccw', 'cruise', 'phi', 200)
                phi_current=0.
                time.sleep(2)
                for j in range(len(phi_step)):
                    if j !=0:
                        self.pcomm.move('can0', self.selected_can, 'cw', 'cruise', 'phi', phi_step[j])
                    phi_current=phi_current+phi_step[j]
                    input('theta '+str(theta_current)+', phi '+str(phi_current)+'\n Press Any Key to Continue... ')
            print('Test Finished for posid '+self.selected_posid+'\nGo to the GUI to move on.')
 
