    def get_list2(self,event):
        index = self.listbox2.curselection()
        self.selected_file=[]
        self.selected_file.append(pc.dirs['xytest_data']+self.listbox2.get(index[0]))
        self.data=Table.read(self.selected_file[0],format='ascii.csv',header_start=0,data_start=1)

        dist=np.sqrt((self.data['target_x']-self.offset_x)**2+(self.data['target_y']-self.offset_y)**2)
        ind1=np.where(dist>self.pos_conf['LENGTH_R1']+self.pos_conf['LENGTH_R2'])
        ind2=np.where(dist<abs(self.pos_conf['LENGTH_R1']-self.pos_conf['LENGTH_R2']))
        ind3=np.where(self.data['err_xy1']>0.1)
        n=len(self.data)
        n1=len(ind1[0])
        n2=len(ind2[0])
        n3=len(ind3[0])-n1-n2
        self.text2.insert(tkinter.END,str(len(self.data))+' targets in total \n')
        self.text2.insert(tkinter.END,str(len(ind1[0]))+' targets outside outer boundary \n')
        self.text2.insert(tkinter.END,str(len(ind2[0]))+' targets inside innter boundary \n')
        self.text2.insert(tkinter.END,str(n3)+' targets not reached with error less than 100micron \n')
        self.text2.insert(tkinter.END,str(float(n-n1-n2-n3)/float(n-n1-n2))+' sources reach target \n')




    def plot(self):
        pos_conf1 = self.pos_conf
        posid1=self.selected[0]
        posmodel1=self.ptl.posmodels[posid1]
        trans1 = posmodel1.trans
        offset_X1=pos_conf1['OFFSET_X']
        offset_Y1=pos_conf1['OFFSET_Y']
        r1=pos_conf1['LENGTH_R1']
        r2=pos_conf1['LENGTH_R2']

        font = {'family' : 'sans-serif',
                 'weight' : 'normal',
                 'size'   : 9}
        plt.rc('font', **font)
        head_width=0.1
        head_length=0.15
        fontsize=25
        plt.figure(1,figsize=(9.5,9.5))
        plt.rc('font', **font)
        plt.subplot(111)
        axes = plt.gca()
        axes.set_xlim([offset_X1-7,offset_X1+7])
        axes.set_ylim([offset_Y1-7,offset_Y1+7])

        theta_min = trans1.posTP_to_obsTP([min(posmodel1.targetable_range_T),0])[0]
        theta_max = trans1.posTP_to_obsTP([max(posmodel1.targetable_range_T),0])[0]
        theta_range = [theta_min,theta_max]
        self.plot_circle([offset_X1,offset_Y1],r1+r2,theta_range)
        self.plot_circle([offset_X1,offset_Y1],abs(r1-r2),theta_range)
        plt.scatter(self.data['target_x'],self.data['target_y'],s=80,facecolors='none', edgecolors='r')
        #plt.plot(self.data['target_x'],self.data['target_y'],'ko')
        plt.plot(self.data['meas_x1'],self.data['meas_y1'],'b+', linewidth=5, markersize=10)
        plt.show()

    def plot_circle(self,center,radius,theta_range):
        annulus_angles = np.arange(0,360,5)*np.pi/180
        annulus_angles = np.append(annulus_angles,annulus_angles[0])
        annulus_outer_x = center[0] + np.abs(radius) * np.cos(annulus_angles)
        annulus_outer_y = center[1] + np.abs(radius) * np.sin(annulus_angles)
        plt.plot(annulus_outer_x,annulus_outer_y,'b-',linewidth=0.5,label='patrol envelope')
        min_line_x = [center[0], radius * np.cos(theta_range[0]*np.pi/180) + center[0]]
        min_line_y = [center[1], radius * np.sin(theta_range[0]*np.pi/180) + center[1]]
        max_line_x = [center[0], radius * np.cos(theta_range[1]*np.pi/180) + center[0]]
        max_line_y = [center[1], radius * np.sin(theta_range[1]*np.pi/180) + center[1]]
        plt.plot(min_line_x,min_line_y,'g-',linewidth=0.5,label='theta min')
        plt.plot(max_line_x,max_line_y,'g--',linewidth=0.8,label='theta max')



    def toggle(self, flag = [0]):
        flag[0] = not flag[0]
        if flag[0]:
            self.pospwr_off()
            self.pwr_button.config(bg='grey')
            self.pwr_button.config(text='POSPWR is OFF')
        else:
            self.pospwr_on()
            self.pwr_button.config(text='POSPWR is ON')
            self.pwr_button.config(bg='green')

